from textual.reactive import reactive
from textual.widgets import Static

class Marking(Static):
    t=reactive('')
    val=reactive(0)
    def __init__(self,datee,val):
        super().__init__()
        self.t=datee
        self.val=val
    def render(self) -> str:
        if self.val==1:
            self.add_class('greener')
        if self.val==0:
            self.add_class('reder')
        return f"{self.t}"
